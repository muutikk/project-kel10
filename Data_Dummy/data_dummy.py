    # Daftar mainan
    toys = ['Boneka', 'Mobil-Mobilan', 'Lego', 'Action Figure']

    # Membuat data dummy produksi dan penjualan
    data = create_production_and_sales_data(years, months, toys)

    # Konversi ke dataframe pandas
    df = pd.DataFrame(data)

